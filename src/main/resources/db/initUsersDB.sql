DROP TABLE IF EXISTS USERS;

CREATE TABLE USERS
(
  id BIGINT AUTO_INCREMENT PRIMARY KEY NOT NULL,
  firstName VARCHAR(64) NOT NULL,
  lastName VARCHAR(64),
  birthday DATETIME,
  email VARCHAR(64) NOT NULL
);
CREATE UNIQUE INDEX users_email_uindex ON users (email);

